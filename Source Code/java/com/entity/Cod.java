package com.entity;

public class Cod {
	private int id;
	private String cod;
	private int cid;

	
	
	public Cod(String cod, int cid) {
		super();
		this.cod = cod;
		this.cid = cid;
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getCod() {
		return cod;
	}

	public void setCod(String cod) {
		this.cod = cod;
	}

	public int getCid() {
		return cid;
	}

	public void setCid(int cid) {
		this.cid = cid;
	}

	
	
	
	
	
	

}
